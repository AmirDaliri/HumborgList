import UIKit

extension UIColor {
    
    @nonobjc class var borderGray: UIColor {
        return UIColor(red: 74.0 / 255.0, green: 74.0 / 255.0, blue: 74.0 / 255.0, alpha: 0.25) //4A4A4A
    }
    
    @nonobjc class var blackTint: UIColor {
        return UIColor(red:0.08, green:0.09, blue:0.11, alpha:1.0) //15161D
    }
}
